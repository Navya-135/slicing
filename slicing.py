#String slicing

name="Rajesh"
print(name[0:2])
print(name[:])
print(name[:5])
print(name[2:])


#reverse a string

name="Navya"
print(name[::-1])

#reverse a string without slicing

name="Malleswari"
name="".join(reversed(name))
print(name)

